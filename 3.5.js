// Math objects

console.log(Math.PI);
// 3.141592653589793
console.log(Math.floor(2.5));
// 2 向下舍得整数
console.log(Math.ceil(2.5));
// 3 向上取得整数
console.log(Math.max(1, 2, 3));
// 3
console.log(Math.pow(2, 3));
// 8 (2 to the power of 3)
console.log(Math.sqrt(4));
// 2
